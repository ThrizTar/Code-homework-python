def love6(first, second):#define love6(first, second) function
    if first == 6 or second == 6 or first + second == 6 or abs(first - second) == 6:#if first equal 6 or second equal 6 or first + second equal 6 or absolute(first - second) equal 6 return True
        return True
    else:#but not return False
        return False
    
def main():#define function main
    first = int(input(""))#input first value by user
    second = int(input(""))#input second value by user
    print(love6(first, second))#show answer 

if __name__=='__main__':
    main()


    





    
    
